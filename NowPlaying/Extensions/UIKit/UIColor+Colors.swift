import UIKit

extension UIColor {
	
	final class var collectionViewBackground: UIColor {
		
		return #colorLiteral(red: 0.9607843137, green: 0.968627451, blue: 0.9921568627, alpha: 1)
		
	}
	
	final class var collectionViewCellBackground: UIColor {
		
		return .lightGray
		
	}
	
	final class var collectionViewCellActivityIndicatorColor: UIColor {
		
		return .darkGray
		
	}
	
	final class var navigationBarTintColor: UIColor {
		
		return .white
		
	}
	
}
